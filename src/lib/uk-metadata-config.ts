// Get UK state metadata by state code
export const getUKStateMetadata = (stateCode: string): UKStateMetadata | undefined => {
  const metadataMap: Record<string, UKStateMetadata> = {
    'england': EnglandMetadata,
    'scotland': ScotlandMetadata,
    'wales': WalesMetadata,
    'northern-ireland': NorthernIrelandMetadata,
  };
  return metadataMap[stateCode.toLowerCase()];
};

// Get UK city metadata by state code and city code
export const getUKCityMetadata = (stateCode: string, cityCode: string): UKCityMetadata | undefined => {
  const citiesMap: Record<string, UKCityMetadata[]> = {
    'england': EnglandCitiesMetadata,
    'scotland': ScotlandCitiesMetadata,
    'wales': WalesCitiesMetadata,
    'northern-ireland': NorthernIrelandCitiesMetadata,
  };
  
  const cities = citiesMap[stateCode.toLowerCase()];
  if (!cities) return undefined;
  
  return cities.find(city => city.cityCode === cityCode.toLowerCase());
};