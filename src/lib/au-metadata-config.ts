// Helper function to get AU state metadata
export const getAUStateMetadata = (stateCode: string) => {
  return AU_STATE_METADATA[stateCode.toLowerCase()];
};

// Helper function to get AU city metadata
export const getAUCityMetadata = (stateCode: string, cityCode: string) => {
  const stateData = AU_CITY_METADATA[stateCode.toLowerCase()];
  if (!stateData) return null;
  return stateData[cityCode.toLowerCase()];
};
